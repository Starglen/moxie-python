# (1)while loop

# increment
num = 55
while num <= 60:
    print(num)
    num += 1

# Decrement
integer = 30
while integer >= 25:
    print(integer)
    integer -= 1

# (2)for loop
for x in range(1, 10, 2):
    print("Number is", x)

devices = ["laptop", "computer", "mouse", "screen"]
for y in devices:
    print(y)