# Arithmetic operators

x= 10
y= 20
print(x+y)
print(x-y)
print(x*y)
print(x/y)
print(x%y) # modulus returns the remainder

# comparison operators
print(x>y)
print(x==y) # equal to
print(x<y)
print(x != y) # not equal to

# Assignment operators
a = 30
print(a)

a += 2
print(a)

# Operator Precedence
number = (6 + 2 * 6 / 5 - 4)
print(number)

# Logical Operators - and, or, not
print(55 < 243 and 23 > 67)
print(5 < 243 or 23 > 67)
print(not(5 < 243 or 23 > 67))
